Государственная регистрация недвижимого имущества, прав на него и сделок с ним не осуществляется в ускоренном или срочном порядке в случае, если в регистрационной книге содержится актуальная отметка о поступившем в организацию по государственной регистрации недвижимого имущества, прав на него и сделок с ним заявлении заинтересованного лица о юридических фактах, в результате которых могут произойти возникновение, переход или прекращение прав, ограничений (обременений) прав на недвижимое имущество.<br/>
В случаях, предусмотренных ст.35 Закона Республики Беларусь «О государственной регистрации недвижимого имущества, прав на него и сделок с ним» от 22.07.2002 №133-З, совершение регистрационного действия может быть приостановлено на срок не более одного месяца. О мотивах и сроках приостановления, порядке обжалования приостановления заявитель уведомляется заказным письмом в течение трех рабочих дней с даты принятия решения о приостановлении совершения регистрационного действия.
