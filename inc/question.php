Согласно Указу Президента Республики Беларусь от 4 августа 2014 г. № 387 "О внесении дополнений и изменений в Указ Президента Республики Беларусь от 27 декабря 2007 г. № 667" (далее – Указ № 387) с 1 января 2015 года в пригородных зонах г. Минска и областных центров запрещаются, если иное не установлено Президентом Республики Беларусь, отчуждение (купля-продажа, мена, дарение) предоставленных в частную собственность для коллективного садоводства земельных участков, в том числе земельных участков, образованных в результате их раздела, слияния, передача в аренду таких земельных участков, передача прав и обязанностей по договорам аренды земельных участков, предоставленных для указанной цели в аренду, в том числе земельных участков, образованных в результате их раздела, слияния, а также отчуждение капитальных строений (зданий, сооружений), незавершенных законсервированных капитальных строений, расположенных на земельных участках, предоставленных для коллективного садоводства, в том числе на земельных участках, образованных в результате их раздела, слияния, до истечения 5 лет со дня государственной регистрации возникновения прав на такие земельные участки, за исключением отчуждения земельных участков и (или) расположенных на них капитальных строений (зданий, сооружений), незавершенных законсервированных капитальных строений местным исполнительным комитетам.<br/>
Предусмотренный данной нормой запрет отчуждения (купля-продажа, мена, дарение) касается исключительно земельных участков, предоставленных гражданину местным исполнительным комитетом для ведения коллективного садоводства в частную собственность, с момента государственной регистрации у гражданина возникновения права частной собственности на которые по состоянию на 1 января 2015 года не истек 5 летний срок.<br/>
Таким образом, запрет на отчуждение после 1 января 2015 года земельных участков не касается земельных участков:<br/>
право частной собственности на которые перешло по наследству (независимо от даты государственной регистрации права частной собственности наследодателя и наследников на земельный участок);<br/>
приобретенных гражданином на основании гражданско-правовых сделок (на основании договоров купли-продажи, дарения, мены и т.д.);<br/>
принадлежащих гражданину на праве пожизненного наследуемого владения либо аренды и выкупленных им в частную собственность, если с момента государственной регистрации возникновения у лица, которому первоначально земельный участок был предоставлен местным исполнительным комитетом для ведения коллективного садоводства, права пожизненного наследуемого владения либо аренды истек 5 летний срок.<br/>
То есть, если гражданин купил в 2014 году или ранее земельный участок для коллективного садоводства и зарегистрировал до 1 января 2015 года в установленном порядке в организации по государственной регистрации недвижимого имущества, прав на него и сделок с ним договор купли-продажи и право собственности на земельный участок, то после 1 января 2015 года гражданин сможет распорядиться этим земельным участком с соблюдением требований законодательства об охране и использовании земель.<br/>
Если же гражданин купил в 2014 году или ранее земельный участок, который был предоставлен продавцу для ведения коллективного садоводства местным исполнительным комитетом, но по состоянию на 1 января 2015 года не пройдет еще 5 лет с момента государственной регистрации возникновения права собственности продавца на этот земельный участок, то в случае обращения покупателя за осуществлением государственной регистрации договора купли-продажи такого земельного участка и основанного на нем права собственности только после 1 января 2015 года, в совершении регистрационных действий покупателю будет отказано, так как будет нарушаться вышеуказанная норма Указа № 387. Разрешение в такой ситуации вопроса об оформлении права собственности на земельный участок возможно будет только в судебном порядке.<br/>
Если же гражданин купил в 2014 году или ранее земельный участок, который был приобретен продавцом на основании гражданско-правовой сделки (договора купли-продажи, мены, дарения ит.д.) или перешел к нему по наследству, то осуществить государственную регистрацию такого договора и основанного на нем права на земельный участок покупатель сможет при отсутствии иных оснований для отказа также после 1 января 2015 года.<br/>
При этом отчуждение гражданами принадлежащих им на праве собственности земельных участков для ведения коллективного садоводства допускается также при отсутствии на них возведенных и зарегистрированных в установленном порядке садовых домиков.<br/>
Аналогичным образом регулируются вопросы отчуждения гражданами садовых домиков, расположенных на земельных участках для коллективного садоводства, принадлежащих им на праве пожизненного наследующего владения либо аренды.

                      								   
