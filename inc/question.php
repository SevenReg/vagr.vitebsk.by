В соответствии с п.22.17.4, 22.17.5, 22.17.6 Перечня административных процедур осуществляемых государственными органами и иными организациями по заявлениям граждан, утвержденного Указом Президента Республики Беларусь от 26.04.2010 №200, гражданину могут быть выданы следующие виды справок:<br/>
<ul>
   <li>справка о принадлежащих лицу правах на объекты недвижимого имущества или справка о правах на объекты недвижимого имущества</li>
   <li>справка об отсутствии прав на объекты недвижимого имущества</li>
   <li>справка о находящихся в собственности гражданина жилых помещениях в соответствующем населенном пункте</li>
</ul>								   
Обращаем  внимание, что справки могут быть выданы по конкретному населенному пункту (например, по г.Витебску) либо по Республике Беларусь.<br/>
Для выдачи любой из вышеуказанных справок гражданин должен предоставить следующие документы: заявление, паспорт или иной документ, удостоверяющий личность, документ об оплате.<br/>
Согласно п.8 инструкции о порядке предоставления сведений и документов в отношении недвижимого имущества, прав на него и сделок с ним организациями по государственной регистрации недвижимого имущества, прав на него и сделок с ним, утвержденной постановлением Комитета по земельным ресурсам, геодезии и картографии при Совете Министров Республики Беларусь №4 от 24.01.2005 (далее - Инструкция №4), указанные выше справки в отношении членов одной семьи могут выдаваться по одному заявлению одного из членов семьи при представлении им документов, содержащих идентификационные сведения в отношении остальных членов семьи (документов, удостоверяющих личность, либо паспортов, либо иных документов, их заменяющих, предназначенных для выезда за границу, иностранного гражданина или лица без гражданства, выданных соответствующим органом государства гражданской принадлежности либо обычного места жительства иностранного гражданина или лица без гражданства или международной организацией, свидетельств о рождении и других). В случае изменения гражданином (членом его семьи) идентификационных сведений и необходимости получения обобщенной информации о принадлежавших ему (члену его семьи) до изменения идентификационных сведений правах на объекты недвижимого имущества, справка с указанием измененных (неактуальных) идентификационных сведений лица выдается в виде отдельного документа.<br/>
Стоимость выдачи одной справки составляет 0.1 базовой величины (2,30 белорусских рублей). Максимальный срок осуществления административных процедур по выдаче справок, указанных выше, составляет 3 рабочих дня со дня подачи заявления.<br/>
Согласно п.3 Инструкции №4 справки могут быть предоставлены по письменному заявлению гражданина, поданному регистратору в ходе личного приема, либо по письменному запросу государственных органов, организаций и иных лиц, имеющих право на получение соответствующих сведений и документов (например, по запросу Витебского городского исполнительного комитета, с которым у нашего предприятия налажена система электронного документооборота при выдаче справок).<br/>
Также заявление может быть направлено посредством почтовой связи или передано в виде электронного документа в порядке, предусмотренном инструкцией о порядке подачи и оформления документов, необходимых для осуществления регистрационных действий, для целей их дальнейшего направления посредством почтовой связи или передачи в виде электронных документов, утвержденной постановлением Государственного комитета по имуществу Республики Беларусь от 16.12.2011 №70. Но направление заявления путем почтовой связи либо в электронном виде потребует дополнительных денежных расходов на нотариальные, почтовые услуги, либо услуги по изготовлению электронной цифровой подписи.<br/>
Для получения справок гражданин вправе лично обратиться с заявлением в РУП «Витебское агентство по государственной регистрации и земельному кадастру» в порядке живой очереди либо по предварительной записи по адресу: 210026, г. Витебск, ул. Политехническая, 5/18, 4 этаж (кабины 6-11), согласно режима работы предприятия: понедельник, суббота: c 8.00 до 17.00 (без перерыва на обед); вторник - пятница: c 8.00 до 20.00 (без перерыва на обед); выходной день - воскресенье. Предварительная запись на любое удобное время может быть осуществлена по телефону 8 (0212) 60-91-30 либо через сайт нашего предприятия www.vagr.vitebsk.by.
