Довольно часто в РУП «Витебское агентство по государственной регистрации и земельному кадастру» (далее – Витебское агентство) обращаются  граждане с просьбой зарегистрировать удостоверенные несколько лет назад в нотариальных конторах договоры купли-продажи, дарения объектов недвижимого имущества. Договоры удостоверены нотариусами в соответствии с требованиями законодательства Республики Беларусь, и вроде бы нет препятствий для государственной регистрации таких договоров и перехода права собственности к покупателю либо одаряемому на объекты недвижимого имущества, но на момент подачи заявления о государственной регистрации сделки уже нет в живых продавца либо дарителя.<br/>
При наличии таких обстоятельств регистраторы недвижимости вынуждены отказывать в государственной регистрации договоров и основанных на них прав в соответствии с пп.1.4 п.1 ст.36 Закона Республики Беларусь от 22 июля 2002 года «О государственной регистрации недвижимого имущества, прав на него и сделок с ним» (далее – Закон о регистрации), поскольку при государственной регистрации нарушаются требования законодательных актов Республики Беларусь, а именно: п.2 ст.16 Гражданского кодекса Республики Беларусь, согласно которому правоспособность гражданина прекращается его смертью. В таких случаях единственным выходом для покупателя либо одаряемого является обращение в суд с иском о признании договора действительным и о признании за ним права собственности на объект недвижимого имущества, что естественно влечет существенные расходы.<br/>
Чтобы не попасть в подобную ситуацию, нужно своевременно обращаться за государственной регистрацией договора и основанных на нем прав и всегда помнить, что в соответствии со ст.8, 9 Закона о регистрации договоры купли-продажи, дарения объектов недвижимого имущества, равно как и любые иные договоры об отчуждении объектов недвижимого имущества считаются заключенными с момента их государственной регистрации, а основанные на таких договорах права возникают, переходят, прекращаются с момента государственной регистрации соответственно их возникновения, перехода, прекращения в территориальной организации по государственной регистрации.<br/>
При обращении за удостоверением договора дарения или купли-продажи объекта недвижимого имущества к регистратору недвижимости Витебского агентства (минуя нотариальную контору) у него на приеме сторонами сделки одновременно могут быть поданы как заявление об удостоверении договора, так и заявление о государственной регистрации договора и основанных на нем прав на объекты недвижимого имущества. Обращаясь в Витебское агентство (его структурные подразделения) за удостоверением договора, Вы точно не забудете подать заявление о его государственной регистрации.<br/>
