		  <div class="ctext1"><br>
   &nbsp;&nbsp;&nbsp;&nbsp;C 7 по 9 февраля в РУП "Витебское агентство по государственной регистрации и земельному кадастру" прошла VII зимняя областная спартакиада. В спартакиаде приняли участие команды: Витебского агентства, Полоцкого, Оршанского, Лепельского и Глубокского филиалов. В селедующих видах спорта: <br /><br />
      <ul>
	     <li>Лыжные гонки мужчины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте старше 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте старше 45 лет;</li>
		 <li>Лыжная эстафета (возраст участников не ограничивался, участие принимали смешанные команды по 2-ое мужчин и 2-е женщин);</li>
		 <li>Гиревый спорт среди мужчин;</li>
		 <li>Бильярд среди мужчин;</li>
		 <li>Бильярд среди женщин;</li>
		 <li>Настольный тенис среди мужчин;</li>
		 <li>Настольный тенис среди женщин;</li>
		 <li>Футбол;</li>
		 <li>Дартс среди мужчин;</li>
		 <li>Дартс среди женщин;</li>
	  </ul>
	  <br>
   &nbsp;&nbsp;&nbsp;&nbsp;Победу в Общекомандном зачете в упорной борьбе одержала команда РУП "Витебское агентство по государственной регистрации и земельному кадастру" общее количество баллов 172. Второе место занимает каманда Полоцкого филиала 152 баллов. На третьем месте команда Оршанского филиала 120 баллов. Команда Глубокского филиала занимает четвертое место 68. И замыкает таблицу команда Лепельского филиала с суммой баллов 54.<br><br>
