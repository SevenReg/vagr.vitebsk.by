       &nbsp;&nbsp;&nbsp;&nbsp;РУП «Витебское агентство по государственной регистрации и земельному кадастру» и его филиалы, расположенные в г. Полоцке, Орша, Глубокое, Лепель оказывают услуги по проведению независимой оценки движимого и недвижимого имущества: капитальных строений (зданий и сооружений, изолированных помещений, не завершенных строительством объектов), земельных участков, машин, оборудования, инвентаря, транспортных средств, материалов и другого имущества. <br />
   &nbsp;&nbsp;&nbsp;&nbsp;В штате предприятия числится 16 оценщиков. В соответствии с Указом Президента Республики Беларусь № 615 от 13.1.2006 г. специалисты нашего предприятия аттестованы Государственным комитетом по имуществу Республики Беларусь и имеют свидетельства об аттестации имущества:  по оценке капитальных строений, оборудования,  земельных участков. Оценщики постоянно повышают свой профессиональный уровень, принимают участие в семинарах и конференциях по оценке имущества: "Новое в оценке объектов недвижимости", "О некоторых изменениях законодательства, регулирующего оценку капитальных строений (зданий, сооружений), не завершенных строительством объектов, изолированных помещений как объектов недвижимого имущества", "Использование информационных ресурсов регистра стоимости и реестра цен в целях оценки недвижимого имущества", "Распоряжение государственным имуществом и особенности его оценки".<br/>
 &nbsp;&nbsp;&nbsp;&nbsp;Стоимость услуг по определению стоимости определяется исходя из фактических трудозатрат согласно Прейскуранта на услуги по оценке, утверждаемого приказами приказами РУП "Витебское агентство по государственной регистрации и земельному кадастру".
 &nbsp;&nbsp;&nbsp;&nbsp;Высокая квалификация, большой штат сотрудников позволяют качественно оказывать услуги в оптимальные сроки.<br>
<br/>
<b>&nbsp;&nbsp;&nbsp;&nbsp;РУП «Витебское агентство по государственной регистрации и земельному кадастру» сообщает о получении специального разрешения (лицензии) №07050/69 на осуществление судебно-экспертной деятельности</b>, в связи с чем, с 28.12.2016 приступило к выполнению нового вида деятельности – судебно - эксперной деятельности по виду (подвиду) указанному в специальном разрешении (лицензии) -  судебной экспертизе по вопросам оценки стоимости гражданских прав: капитальных строений, (зданий, сооружений), изолированных помещений, машино-мест, не завершенных строительством объектов и имущественных прав на них.<br/>
&nbsp;&nbsp;&nbsp;&nbsp;Судебно - экспертная деятельность осуществляется оценщиками недвижимости, имеющими свидетельства о присвоении  квалификации судебного эксперта.<br/> 
&nbsp;&nbsp;&nbsp;&nbsp;Предлагаем обращаться за осуществлением судебной экспертизы по вопросам оценки стоимости гражданских прав: капитальных строений, (зданий, сооружений), изолированных помещений, машино-мест, не завершенных строительством объектов и имущественных прав на них по адресу: 210026, г. Витебск, ул. Политехническая, 5/18, а также в Поставское бюро Глубокского филиала по адресу: 211840, Витебская область, г.Поставы, ул.Юбилейная, 8.
Будем рады оказывать услуги судебной экспертизы качественно и в кратчайшие сроки.

