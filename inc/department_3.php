 <b>Оказываются следующие услуги по проведению оценки:</b>
<UL>
<LI>независимая оценка для приватизации жилых помещений по Указу Президента Республики Беларусь от 25.01.2016 №25;</LI>
<LI>капитальных строений (зданий и сооружений), не завершенных строительством объектов, изолированных помещений как объектов недвижимого имущества;</LI>
<LI>машин, оборудования, инвентаря, материалов;</LI>
<LI>транспортных средств;</LI>
<LI>земельных участков.</LI>
</UL><br>
<b>Определение стоимости объектов оценки и подготовка документов оценки проводится в целях:</b>
<UL>
<LI>продажи без проведения аукциона либо конкурса, включая продажу имущества в процедуре экономической несостоятельности (банкротства);</LI>
<LI>внесения в виде неденежного вклада в уставный фонд юридического лица;</LI>
<LI>продажи на торгах (аукционе и по конкурсу), в том числе в процедуре банкротства;</LI>
<LI>обязательной переоценки методом прямой оценки с привлечением оценщиков;</LI>
<LI>передачи в залог (ипотеку);</LI>
<LI>безвозмездного отчуждения;</LI>
<LI>безвозмездной передачи;</LI>
<LI>передачи в безвозмездное пользование аренду;</LI>
<LI>передачи в доверительное управление;</LI>
<LI>мены;</LI>
<LI>наследования имущества;</LI>
<LI>разрешений имущественных споров;</LI>
<LI>возмещения убытков(ущерба);</LI>
<LI>реализации управленческих решений, за исключением перечисленного выше;</LI>
<LI>постановки на баланс излишков активов, выявленных в результате инвентаризации, и активов, полученных безвозмездно;</LI>
<LI>разделения (выделения) первоначальной стоимости инвентарного объекта;</LI>
<LI>иных, не противоречащих законодательству.</LI>
</UL>
В результате работы Заказчик получает Заключение о стоимости оцениваемого объекта и Отчет об оценке в полной или в краткой форме, содержание и оформление которых соответствует требованиям действующего законодательства Республики Беларусь и Международным стандартам оценки. 
<P><br>
<b>Заключение об оценке содержит:</b>
<OL>
<LI>Полное наименование исполнителя оценки. 
<LI>Фамилию, имя и отчество оценщика, ответственного за проведение оценки, номер и срок действия свидетельства об аттестации оценщика по соответствующему виду объекта оценки. </LI>
<LI>Номер и дату заключения договора на проведение независимой оценки либо указание на судебное постановление. </LI>
<LI>Описание объекта оценки (техническое и иное, включая перечень ограничений (обременений) прав на объект оценки при их наличии). </LI>
<LI>Цель оценки. </LI>
<LI>Дату оценки. </LI>
<LI>Валюту оценки. </LI>
<LI>Названия используемых методов оценки. </LI>
<LI>Итоговую стоимость объекта оценки. </LI>
<LI>Иную информацию по соглашению сторон, а также по усмотрению исполнителя оценки. </LI>
</OL><br>
<b>Отчет об оценке в полной форме содержит:</b>
<OL>
<LI>Титульный лист с указанием наименования и адреса объекта оценки. </LI>
<LI>Краткое содержание основных фактов и выводов. </LI>
<LI>Дату проведения осмотра объекта оценки. </LI>
<LI>Дату оценки. </LI>
<LI>Цены оценки. </LI>
<LI>Валюту оценки. </LI>
<LI>Цель оценки. </LI>
<LI>Предмет оценки. </LI>
<LI>Основные предпосылки и ограничения. </LI>
<LI>Анализ данных, используемых для оценки. </LI>
<LI>Анализ местоположения объекта оценки. </LI>
<LI>Описание объекта оценки. </LI>
<LI>Методы оценки и методы расчета стоимости, обоснование их применения. </LI>
<LI>Описание процедуры оценки. </LI>
<LI>Обоснование итоговой стоимости. </LI>
<LI>Ссылка на используемые стандарты.</LI> 
<LI>Список используемой литературы и ТНПА.</LI> 
<LI>Перечень данных, используемых при оценке с указанием их источника. </LI>
<LI>Общие сведения об исполнителе оценки и оценщиках. </LI>
<LI>Общие данные о заказчике. </LI>
<LI>Приложения. </LI>
<LI>Другие данные, необходимые для проведения оценки. </LI>
</OL><br>
<b>Отчет об оценке в краткой форме содержит:</b>
<OL>
<LI>Основную информацию, использованную при проведении оценки. </LI>
<LI>Краткое обоснование использованных методов оценки или методов расчета стоимости. </LI>
<LI>Описание процедуры оценки. </LI>
<LI>Результаты оценки.</LI>
</OL> 
В краткий отчет об оценке не включается информация, представляющая коммерческую и служебную тайну.
