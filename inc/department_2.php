<b>Управлением "Регистр" осуществляется:</b><br/><br/>
<UL>
<LI>государственная регистрация объектов государственной регистрации, указанных в статье 4 Закона Республики Беларусь от 22 июля 2002 года «О государственной регистрации недвижимого имущества, прав на него и сделок с ним»;</LI> 
<LI>предоставление сведений и документов в отношении недвижимого имущества, прав на него и сделок с ним из единого государственного регистра недвижимого имущества, прав на него и сделок с ним;</LI>
<LI>внесение исправлений в документы единого государственного регистра недвижимого имущества, прав на него и сделок с ним;</LI>
<li>удостоверение документов, являющихся основанием для государственной регистрации сделки с недвижимым имуществом, из которых явствует волеизъявление гражданина или индивидуального предпринимателя, действующего без печати. </li>
</UL>
