		  <div class="ctext1"><br>
   &nbsp;&nbsp;&nbsp;&nbsp;C 13 по 15 февраля 2015 года в РУП "Витебское агентство по государственной регистрации и земельному кадастру" прошла VIII зимняя областная спартакиада на базе турбазы "Лосвидо". В спартакиаде приняли участие команды: Витебского агентства, Полоцкого, Оршанского, Лепельского и Глубокского филиалов. Программма спартакиды включала: <br /><br />
      <ul>
	     <li>Лыжные гонки мужчины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте старше 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте старше 45 лет;</li>
		 <li>Лыжная эстафета (возраст участников не ограничивался, участие принимали смешанные команды по 2-ое мужчин и 2-е женщин);</li>
		 <li>Гиревый спорт среди мужчин;</li>
		 <li>Бильярд среди мужчин;</li>
		 <li>Настольный тенис среди мужчин;</li>
		 <li>Настольный тенис среди женщин;</li>
		 <li>Футбол;</li>
		 <li>Дартс среди мужчин;</li>
		 <li>Дартс среди женщин;</li>
         <li><b>Конкурс художетвенной самодеятельности, посвященный 70-летию Великой Победы в Великой Отечественной войне</b></li>
	  </ul>
	  <br>
   &nbsp;&nbsp;&nbsp;&nbsp;Победу в Общекомандном зачете в упорной борьбе одержала команда РУП "Витебское агентство по государственной регистрации и земельному кадастру" общее количество баллов 166. Второе место занимает каманда Полоцкого филиала 136 баллов. На третьем месте команда Оршанского филиала 102 баллов. Команда Глубокского филиала занимает четвертое место 72. И замыкает таблицу команда Лепельского филиала с суммой баллов 67.<br><br>
