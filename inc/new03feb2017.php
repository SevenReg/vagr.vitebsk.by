    <br/>
    &nbsp;&nbsp;&nbsp;  На основании решения Витебского областного исполнительного комитета от 24 января 2017 года № 30 в номинации «Сфера услуг» <b>присвоено почетное звание «Человек года Витебщины» директору РУП «Витебское агентство по государственной регистрации и земельному кадастру» Яковлеву Леониду Сергеевичу.</b><br/>
