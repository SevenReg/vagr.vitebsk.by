		   <li>Покупка, продажа жилых домов, коттеджей, дач в Витебске, Полоцке, Новополоцке, Орше и пригородах;</li>
           <li>Покупка объектов недвижимости с использованием банковских кредитов;</li>
           <li>Полный комплекс с услуг по оформлению сделок (проведение расчетов по сделкам, содействие в нотариальном  оформлении (либо удостоверения сделки  у регистратора недвижимости), государственной регистрации;</li>
           <li>Подбор и продажа квартир, домой, коттеджей в Витебске, Орше,Полоцке, Новополоцке для иногородних, иностранцев, граждан Росси и СНГ.</li>
	    </ol>
	  <br /><br/>
      
<b>В отделе работают аттестованные специалисты, имеющие высокую профессиональную квалификацию в области сделок с объектами недвижимого имущества: риэлтеры, агенты по операциям с недвижимостью.</b>
<br /><br />
   <b>Опытные специалисты отдела для Вас:</b>
   <ul>
      <li>предоставят консультацию по всем операциям с недвижимостью;</li>
      <li>окажут риэлтерские услуги: покупка и продажа квартир, домов (в том числе продажа коттеджей), комнат, земельных участков, объектов нежилого фонда;</li>
      <li>обеспечат комплексное сопровождение сделок с недвижимостью;</li>
      <li>организуют по желанию Потребителя удостоверение сделок у нотариусов, регистраторов недвижимости и их регистрацию;</li>
      <li>помогут в изготовлении технических документов на объекты недвижимости</li>
   </ul>
