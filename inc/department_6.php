      <b>Услуги отдела:</b> <br />
	    <ol>
	       <li>Покупка, продажа, обмен (мена) объектов недвижимости;</li>
		   <li>Проведение эффективной рекламной компании объектов недвижимости в специализируемых изданиях и в Интернете (сайт агентства-www.gosrielt.by.) за счет Предприятия (при условии заключения договора на оказание риэлтерских услуг);</li>
