Риэлтерской деятельностью в Республике Беларусь в соответствии с Указом Президента Республики Беларусь от 09 .01.2006г. №15 «О риэлтерской деятельности в Республике Беларусь» (далее – Указ №15) является посредническая деятельность коммерческой организации по содействию при заключении, исполнении, прекращении договоров на строительство (в том числе долевое), купли-продажи, мены, аренды, иных сделок с объектами недвижимости, правами на них (за исключением организации и проведения аукционов и конкурсов), в том числе:
       <ul>
          <li>консультационные услуги по сделкам с объектами недвижимости;</li>
          <li>представление информации о спросе и предложении на объекты недвижимости;</li>
          <li>подбор вариантов сделки с объектом недвижимости, сделки с участием в строительстве объекта недвижимости;</li>
          <li>организация и проведение согласования условий предстоящей сделки;</li>
          <li>помощь в подготовке (оформлении) документов, связанных с осуществлением сделки с объектом недвижимости, сделки с участием в строительстве объекта недвижимости, регистрации недвижимого имущества, прав на него и сделок с ним.</li>
       </ul>
       <b>
        &nbsp;&nbsp;&nbsp; Услуги, указанные в Указе №15, составляющие риэлтерскую деятельность, являются риэлтерскими услугами.<br />
Республиканское унитарное предприятие «Витебское агентство по государственной регистрации и земельному кадастру» (далее – Предприятие) в соответствии с приказом Государственного комитета по имуществу Республики Беларусь от 14 апреля 2015г. №83 реорганизовано путём присоединения к нему Республиканского дочернего унитарного предприятия «Витебское агентство по оказанию риэлтерских услуг»</b> и является правопреемником всех его прав и обязанностей. <br />
В процессе реорганизации <b>создан отдел риэлтерских услуг.</b>

       <br/><br/>
