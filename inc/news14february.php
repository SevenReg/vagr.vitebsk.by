		  <div class="ctext1"><br>
   &nbsp;&nbsp;&nbsp;&nbsp;C 11 по 13 февраля в РУП "Витебское агентство по государственной регистрации и земельному кадастру" прошла IV зимняя областная спартакиада. В спартакиаде приняли участие команды: Витебского агентства, Полоцкого, Оршанского, Лепельского и Глубокского филиалов. В селедующих видах спорта: <br /><br />
      <ul>
	     <li>Лыжные гонки мужчины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 30 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте до 45 лет;</li>
		 <li>Лыжные гонки мужчины в возрасте старше 45 лет;</li>
		 <li>Лыжные гонки женщины в возрасте старше 45 лет;</li>
		 <li>Лыжная эстафета (возраст участников не ограничивался, участие принимали смешанные команды по 2-ое мужчин и 2-е женщин);</li>
		 <li>Гиревый спорт среди мужчин;</li>
		 <li>Гиревый спорт среди женщин;</li>
		 <li>Бильярд среди мужчин;</li>
		 <li>Бильярд среди женщин;</li>
		 <li>Настольный тенис среди мужчин;</li>
		 <li>Настольный тенис среди женщин;</li>
		 <li>Футбол;</li>
		 <li>Дартс среди мужчин;</li>
		 <li>Дартс среди женщин;</li>
	  </ul>
	  <br>
   &nbsp;&nbsp;&nbsp;&nbsp;Победу в Общекомандном зачете в упорной борьбе одержала команда РУП "Витебское агентство по государственной регистрации и земельному кадастру" общее количество баллов 208. Второе место занимает каманда Оршанского филиала 130 баллов. На третьем месте команда Полоцкого филиала 100 баллов. Команда Лепельского филиала занимает четвертое место уступив Полоцку всего 4 балла общая сумма баллов 96. И замыкает таблицу команда Глубокского филиала с суммой баллов 60.<br><br>
